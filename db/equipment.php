<b>A COMPUTER, A TNC, A RADIO....<BR>
<b>Your tnc will have an instruction manual.  There will be a cable or
a plug that will come with it.  You will need to hook up PTT, REC AUDIO,
GROUND, AND TRANSMIT AUDIO TO THE APPROPRIATE PINS ON THE CONNECTOR.  ALL OF THESE ARE CONNECTIONS TO YOUR RADIO!!!!
<P><b>
Once the tnc is connected to the radio, you will need to disconnect your
microphone, the software and the computer will control your radio and
the mike will be open when in the transmit mode and if the stereo is
on you will transmit whatever is playing on the stereo and i do not think
