<h2>Amateur Radio Email Primer</h2>

<i>Definitions</i><P>
<B>Pactor - this is nothing more than a mode that uses both upper and lower case characters and teleprints over radio, using a code that is built in that gives 100% error free copy.  It is basically a combination of amtor (amateur teleprinting over radio) and packet.  Pactor uses acknowledge and not acknowledge codes that are built in.  Common modes are pactor I and pactor II.<P>
<B>tnc - this stands for terminal node controller.  Just think of it as a radio modem.<P>
<B>ptt - that is the term push to talk.  It is what makes your radio transmit.<P>
<B>rec audio - this is your receive signal that goes to your speaker after the radio does its magic<P>
<b>transmit audio - when you talk in to your mike, you create transmit audio<P>
<B>ground - ground is ground the world around!!!! ( - pole on battery)<P>
<B>mike ground - usually shielded cable in some mike cords for shielding the transmit audio<P>
<B>software - the software that makes cruising e-mail work<P>
<B>ISP/radio e-mail provider - this is the station that you will use for the actual access in and out of the internet.<P>

<b>
To tie all this together, you must have a well installed radio system
on your boat!!!  I cannot stress this more.  If you have ssb on your
boat and have trouble contacting stations or can not hear stations very
well, then you need to enhance your station.  The single most common
area of dismal performance is in your RF ground.  If you have a tuner
and think that a piece of ten guage wire from the tuner to the dynaplate
is sufficient, save your money and forget digital e-mail, it will simply
will frustrate you.  There is an excellent publication available from
ICOM and it is free, call their technical department and they will send
you a copy.  It covers the installation of a proper rf ground on your boat.  Of all the boats that I have visited that have problems, a good rf ground solved the problem!
<P>
<b>You will need a computer that will run windows 95/98.  There still are
some stations that will work in dos but are getting rare.
<P><b>
The hookup!!!!!<a target="_blank" href="http://www.phoenixcon.net/~pelago/index.html"><IMG SRC="/images/pelago.jpg" border="0" height="242" width="300" align="right"></a>
